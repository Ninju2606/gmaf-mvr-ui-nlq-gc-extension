public class Colors {
	private static Color blue = new Color(62, 155, 213);
	private static Color dark = new Color(50, 50, 50);
	private static Color light = new Color(215, 215, 215);
	private static Color gray = new Color(60, 60, 60);
	private static Color green = new Color(62, 215, 143);
	private static Color red = new Color(215, 62, 62);
	private static Color teal = new Color(62, 215, 189);

	public static Color getBlue() {
		return blue;
	}

	public static Color getDark() {
		return dark;
	}

	public static Color getLight() {
		return light;
	}

	public static Color getGray() {
		return gray;
	}

	public static Color getGreen() {
		return green;
	}

	public static Color getRed() {
		return red;
	}

	public static Color getTeal() {
		return teal;
	}

}
